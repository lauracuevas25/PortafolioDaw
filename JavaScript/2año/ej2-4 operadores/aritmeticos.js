console.log('SUMA: 5 + 4.5 = ' + (5 + 4.5)); // SUMA. Devuelve 9.5 (Number)
console.log('RESTA: 5 - 4.5 = ' + (5 - 4.5)); // RESTA. Devuelve 0.5 (Number)
